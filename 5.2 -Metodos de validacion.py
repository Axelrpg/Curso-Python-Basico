# Metodos de validacion

palabra = "12345"

# Verificar si inicia con
print(palabra.startswith("manejo"))

# Verificar si termina con
print(palabra.endswith("python"))

# Verificar si es alfanumerico (sin espacios)
print(palabra.isalnum())

# Verificar si es alfabetico (sin espacios)
print(palabra.isalpha())

# Verificar si son digitos
print(palabra.isdigit())

# Verificar si estan en minuscula
print(palabra.islower())

# Verificar si estan en mayuscula
print(palabra.isupper())

# Verificar si es un espacio
print(palabra.isspace())